  # Lecture no 9 Comparison Operators in python
x = 10
y = 20
print(x==y)   # x equal hai y k ( false )


print(x!=y)   # x equal nahi hai y k ( true )
print(x>y)    # x bara hai y k  ( false )
print(x<y)    # x chota hai y k  ( true )
print(x>=y) # x bara hai yaa braber hai y k(false)
print(x<=y)# x chorta hai yaa braber hai y k(true)
